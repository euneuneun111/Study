package test;

// 클래스 정의: Datatype03 클래스 선언
public class Datatype03 {

   // main 메서드: 자바 프로그램의 시작점
   public static void main(String[] args) {

      // char형 변수 ch 선언 및 초기화 (문자 1개 저장, 작은 따옴표 사용)
      char ch = 'J';  // 대문자 J

      // char형 변수 ch2 선언 및 초기화
      char ch2 = 'a'; // 소문자 a

      // char형 변수 ch3 선언 및 초기화
      char ch3 = 'v'; // 소문자 v

      // char형 변수 ch4 선언 및 초기화
      char ch4 = 'a'; // 소문자 a

      // 각각의 문자를 출력
      System.out.println(ch);   // J 출력
      System.out.println(ch2);  // a 출력
      System.out.println(ch3);  // v 출력
      System.out.println(ch4);  // a 출력
   }
}
